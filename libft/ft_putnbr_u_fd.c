#include "libft.h"

void	ft_putnbr_u_fd(unsigned int n, int fd)
{
	char			x;

	if (n >= 10)
		ft_putnbr_u_fd(n / 10, fd);
	x = n % 10 + '0';
	write(fd, &x, 1);
}
